/**
 * @param {string} num1
 * @param {string} num2
 * @return {string}
 */
var addStrings = function (num1, num2) {
  let s1 = num1.split('').reverse()
  let s2 = num2.split('').reverse()
  let a = 0
  let res = []
  for (let i = 0; i < s1.length || i < s2.length; i++) {
    let sum = (+s1[i] || 0) + (+s2[i] || 0) + a
    res.push(sum % 10)
    a = sum >= 10 ? 1 : 0
  }
  if (a) {
    res.push(1)
  }
  return res.reverse().join('')
};
